void ofApp::draw(){

    ofBackground(0, 0, 0);
    ofSetColor(255, 255, 255, 255);
    
    for(int x = 0; x<= 1024; x += 128){
        ofLine(x, 0, x, 768);
    }
    for(int y = 0; y<= 768; y += 128){
        ofLine(0, y, 1024, y);
    }
    
    ofCircle(128*3, 128*1, 128*1-28*1);
    ofEllipse(128*1, 128*3, 128*2-28*2, 128*6-28*2);
    ofRect(128*4, 128*2, 128*2, 128*2);
    ofTriangle(128*2+28, 128*4+28,
               128*2+28, 128*6-28,
               128*4-28, 128*6-28);

}

